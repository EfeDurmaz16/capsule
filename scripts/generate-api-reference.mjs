import { existsSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { join, relative } from "node:path";

const root = process.cwd();
const packagesDir = join(root, "packages");
const outputPath = join(root, "docs", "api-reference.md");

function readJson(path) {
  return JSON.parse(readFileSync(path, "utf8"));
}

function exportedSymbols(source) {
  const symbols = new Set();
  const exportDecl = /^export\s+(?:declare\s+)?(?:async\s+)?(?:class|function|const|let|var|interface|type|enum)\s+([A-Za-z0-9_]+)/gm;
  const reExportDecl = /^export\s+\{([^}]+)\}/gm;
  const starExportDecl = /^export\s+\*\s+from\s+["'](.+)["'];?/gm;
  let match;

  while ((match = exportDecl.exec(source))) {
    symbols.add(match[1]);
  }
  while ((match = reExportDecl.exec(source))) {
    for (const part of match[1].split(",")) {
      const cleaned = part.trim().replace(/^type\s+/, "");
      if (!cleaned) continue;
      const aliasMatch = cleaned.match(/\s+as\s+([A-Za-z0-9_]+)$/);
      symbols.add(aliasMatch?.[1] ?? cleaned.split(/\s+/)[0]);
    }
  }
  while ((match = starExportDecl.exec(source))) {
    symbols.add(`* from ${match[1]}`);
  }

  return [...symbols].sort((a, b) => a.localeCompare(b));
}

function publicPackages() {
  return readdirSync(packagesDir)
    .map((dir) => join(packagesDir, dir))
    .filter((dir) => existsSync(join(dir, "package.json")))
    .map((dir) => ({ dir, pkg: readJson(join(dir, "package.json")) }))
    .filter(({ pkg }) => pkg.private !== true)
    .sort((a, b) => a.pkg.name.localeCompare(b.pkg.name));
}

const lines = [
  "# API Reference",
  "",
  "Generated by `pnpm docs:api` from public package entrypoints. Commit this file intentionally when the public API surface changes.",
  "",
  "This is a lightweight reference for maintainers and adapter authors. It lists package entrypoints and exported symbols; TypeScript declaration files remain the source of truth for full signatures.",
  ""
];

for (const { dir, pkg } of publicPackages()) {
  const sourcePath = join(dir, "src", "index.ts");
  const source = existsSync(sourcePath) ? readFileSync(sourcePath, "utf8") : "";
  const symbols = exportedSymbols(source);
  lines.push(`## ${pkg.name}`, "");
  lines.push(`- Package: \`${pkg.name}\``);
  lines.push(`- Entrypoint: \`${pkg.exports?.["."]?.import ?? pkg.main ?? "./dist/index.js"}\``);
  lines.push(`- Types: \`${pkg.exports?.["."]?.types ?? pkg.types ?? "./dist/index.d.ts"}\``);
  lines.push(`- Source: \`${relative(root, sourcePath)}\``);
  lines.push("");
  if (symbols.length === 0) {
    lines.push("Exports: no symbols detected from `src/index.ts`.");
  } else {
    lines.push("Exports:");
    for (const symbol of symbols) {
      lines.push(`- \`${symbol}\``);
    }
  }
  lines.push("");
}

writeFileSync(outputPath, `${lines.join("\n").trimEnd()}\n`);
console.log(`Wrote ${relative(root, outputPath)}`);
